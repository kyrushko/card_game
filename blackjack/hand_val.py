def hand_value(hand):
    value = 0
    aces = 0
    for i in hand:
        rank, suit = i
        if rank in ['J', 'Q', 'K']:
            value += 10
        elif rank == 'A':
            aces += 1
            value += 11
        else:
            value += int(rank)
    while value > 21 and aces:
        value -= 10
        aces -= 1
    return value
